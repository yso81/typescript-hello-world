#!/usr/bin/env python3
"""
Function that takes 2 strings as arguments and returns a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Function that concatenates str1 and str2

    Args:
    str1: str
    str2: str

    Return:
    str: str1 + str2
    """
    return str1 + str2
